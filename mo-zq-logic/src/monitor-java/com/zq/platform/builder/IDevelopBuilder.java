package com.zq.platform.builder;

//============================================================
// <T>开发编译接口。</T>
//============================================================
public interface IDevelopBuilder
{
   //============================================================
   // <T>获得有效性。</T>
   //
   // @return 有效性
   //============================================================
   boolean isValid();

   //============================================================
   // <T>获得间隔。</T>
   //
   // @return 间隔
   //============================================================
   int interval();

   //============================================================
   // <T>初始化处理。</T>
   //============================================================
   void initialize();

   //============================================================
   // <T>执行处理。</T>
   //
   // @return 处理结果
   //============================================================
   boolean process();
}
