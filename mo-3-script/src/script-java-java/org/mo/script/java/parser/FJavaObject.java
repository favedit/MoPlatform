package org.mo.script.java.parser;

import org.mo.com.lang.FObject;

//============================================================
// <T>基础对象。</T>
//============================================================
public class FJavaObject
      extends FObject
{
   // 类名称
   protected String _name;

   //============================================================
   // <T>构造基础对象。</T>
   //============================================================
   public FJavaObject(){
   }

   //============================================================
   // <T>获得名称。</T>
   //
   // @return 名称
   //============================================================
   public String name(){
      return _name;
   }

   //============================================================
   // <T>设置名称。</T>
   //
   // @param name 名称
   //============================================================
   public void setName(String name){
      _name = name.trim();
   }
}
